# ==================== 年龄段定义 ====================
AGE_GROUPS = {
    "infant": {"name": "婴儿期", "min_months": 0, "max_months": 12, "description": "0-12个月"},
    "toddler": {"name": "幼儿期", "min_months": 12, "max_months": 36, "description": "1-3岁"},
    "preschool": {"name": "学龄前期", "min_months": 36, "max_months": 72, "description": "3-6岁"},
    "school_age": {"name": "学龄期", "min_months": 72, "max_months": 144, "description": "6-12岁"},
}

def get_age_group(months: int) -> str:
    """根据月龄获取年龄段"""
    for group_id, group in AGE_GROUPS.items():
        if group["min_months"] <= months < group["max_months"]:
            return group_id
    return "school_age"  # 默认学龄期

def get_growth_standard(gender: str, metric: str, age_months: int) -> Optional[Dict]:
    """
    获取指定年龄的生长标准值
    
    Args:
        gender: "boys" 或 "girls"
        metric: "weight", "height", "bmi", "head_circumference"
        age_months: 月龄
    
    Returns:
        包含 p3, p50, p97 等百分位数值的字典
    """
    standards = {
        "weight": {
            (0, 12): WHO_INFANT_WEIGHT,
            (12, 36): WHO_TODDLER_WEIGHT,
            (36, 72): CHINA_PRESCHOOL_WEIGHT,
            (72, 144): CHINA_SCHOOL_AGE_WEIGHT,
        },
        "height": {
            (0, 12): WHO_INFANT_HEIGHT,
            (12, 36): WHO_TODDLER_HEIGHT,
            (36, 72): CHINA_PRESCHOOL_HEIGHT,
            (72, 144): CHINA_SCHOOL_AGE_HEIGHT,
        },
        "bmi": {
            (24, 144): WHO_BMI,
        },
        "head_circumference": {
            (0, 24): WHO_HEAD_CIRCUMFERENCE,
        }
    }
    
    if metric not in standards:
        return None
    
    # 找到对应年龄段的标准
    for (min_age, max_age), standard in standards[metric].items():
        if min_age <= age_months < max_age:
            data = standard.get(gender, {})
            months_list = data.get("months", [])
            if age_months in months_list:
                idx = months_list.index(age_months)
                return {
                    "p3": data["p3"][idx],
                    "p50": data["p50"][idx],
                    "p97": data["p97"][idx],
                }
    
    return None

def calculate_percentile(value: float, gender: str, metric: str, age_months: int) -> Optional[float]:
    """
    计算生长指标的百分位
    
    Args:
        value: 实际测量值
        gender: "boys" 或 "girls"
        metric: "weight", "height", "bmi"
        age_months: 月龄
    
    Returns:
        百分位数值 (0-100)，如果无法计算则返回 None
    """
    standard = get_growth_standard(gender, metric, age_months)
    if not standard:
        return None
    
    p3, p50, p97 = standard["p3"], standard["p50"], standard["p97"]
    
    if value < p3:
        return 1.0
    elif value > p97:
        return 99.0
    elif value < p50:
        # 在 p3 和 p50 之间线性插值
        return 3 + (value - p3) / (p50 - p3) * 47
    else:
        # 在 p50 和 p97 之间线性插值
        return 50 + (value - p50) / (p97 - p50) * 47

